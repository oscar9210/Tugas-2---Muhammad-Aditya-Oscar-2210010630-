    private void jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActionPerformed
        try {
                    // Ambil nilai suhu dari JTextField
                    String input = txtSuhu.getText();
                    if (input.isEmpty()) {
                        throw new Exception("Masukkan nilai suhu!");
                    }
                    double suhu = Double.parseDouble(input);

                    // Ambil pilihan konversi dari JComboBox
                    String pilihan = (String) cmbKonversi.getSelectedItem();
                    double hasil = 0;

                    // Hitung hasil konversi berdasarkan pilihan
                    if ("Celcius ke Fahrenheit".equals(pilihan)) {
                        hasil = (suhu * 9 / 5) + 32;
                    } else if ("Celcius ke Kelvin".equals(pilihan)) {
                        hasil = suhu + 273.15;
                    } else if ("Fahrenheit ke Celcius".equals(pilihan)) {
                        hasil = (suhu - 32) * 5 / 9;
                    } else if ("Kelvin ke Celcius".equals(pilihan)) {
                        hasil = suhu - 273.15;
                    }

                    // Tampilkan hasil
                    lblHasil.setText("Hasil: " + hasil);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Masukkan angka yang valid!", 
                        "Error", JOptionPane.ERROR_MESSAGE);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), 
                        "Error", JOptionPane.ERROR_MESSAGE);
                }
    }//GEN-LAST:event_jButtonActionPerformed

